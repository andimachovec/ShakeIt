#include "timerview.h"

//-----------------------------------------------------------------------------
TimerView::TimerView()
		: BView("timerview", B_SUPPORTS_LAYOUT|B_WILL_DRAW|B_PULSE_NEEDED)
//-----------------------------------------------------------------------------
{
	is_running=false;

}


//-----------------------------------------------------------------------------
void TimerView::StartTimer()
//-----------------------------------------------------------------------------
{
	is_running=true;
	
}


//-----------------------------------------------------------------------------
void TimerView::StopTimer()
//-----------------------------------------------------------------------------
{
	is_running=false;
	
}


//-----------------------------------------------------------------------------
void TimerView::Pulse()
//-----------------------------------------------------------------------------
{

	if(is_running)
	{	
		std::cout << "Pulse..." << std::endl;	
	}
}	
