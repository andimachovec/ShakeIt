#include "boggleletter.h"


#include <string>

//-----------------------------------------------------------------------------
BoggleLetter::BoggleLetter(std::string Name)
//-----------------------------------------------------------------------------
{

	name=Name;
	orientation=0;

}


//-----------------------------------------------------------------------------
std::string BoggleLetter::GetName(void)
//-----------------------------------------------------------------------------
{

	return name;

}


//-----------------------------------------------------------------------------
void BoggleLetter::SetOrientation(int Orientation)
//-----------------------------------------------------------------------------
{

	orientation=Orientation;

}


//-----------------------------------------------------------------------------
int BoggleLetter::GetOrientation(void)
//-----------------------------------------------------------------------------
{

	return orientation;

}


